// ç°¡åŒ–èƒŒæ™¯åœ–ç‰‡è¨ˆç®—å‡½æ•¸
function calcContainRect(boxW, boxH, imgW, imgH) {
  if (!imgW || !imgH) return { x: 0, y: 0, w: boxW, h: boxH };
  
  const imgR = imgW / imgH;
  const boxR = boxW / boxH;
  
  if (imgR > boxR) {
    const w = boxW;
    const h = boxW / imgR;
    return { x: 0, y: (boxH - h) / 2, w, h };
  } else {
    const h = boxH;
    const w = boxH * imgR;
    return { x: (boxW - w) / 2, y: 0, w, h };
  }
}

