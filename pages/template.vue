      return;
    }
    
    const cw = container.clientWidth;
    const ch = container.clientHeight;
    const imgR = bgMeta.w / bgMeta.h;
    const boxR = cw / ch;
    
    console.log('å®¹å™¨å°ºå¯¸:', cw, 'x', ch);
    console.log('åœ–ç‰‡æ¯”ä¾‹:', imgR, 'å®¹å™¨æ¯”ä¾‹:', boxR);
    
    if (imgR > boxR) {
      const w = cw;
      const h = cw / imgR;
      containRect.x = 0;
      containRect.y = (ch - h) / 2;
      containRect.w = w;
      containRect.h = h;
    } else {
      const h = ch;
      const w = ch * imgR;
      containRect.x = (cw - w) / 2;
      containRect.y = 0;
      containRect.w = w;
      containRect.h = h;
    }
    
    console.log('å®¹å™¨çŸ©å½¢å·²æ›´æ–°:', containRect);
  });
}

